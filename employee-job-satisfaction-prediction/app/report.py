"""
report.py
Generates a downloadable PDF prediction report using fpdf2, with
canvas-drawn accent elements to keep it visually on-brand.
"""

from datetime import datetime

from fpdf import FPDF

DARK = (11, 15, 26)
PRIMARY = (56, 189, 248)
ACCENT = (167, 139, 250)
TEXT = (30, 41, 59)
MUTED = (100, 116, 139)
LIGHT_BG = (247, 249, 252)

# Core PDF fonts (Helvetica) only support latin-1. Any text coming from
# dynamic sources (recommendations, reasoning, user input) may contain
# unicode punctuation that latin-1 can't encode, so we normalize it first.
_UNICODE_REPLACEMENTS = {
    "\u2014": "-",   # em dash —
    "\u2013": "-",   # en dash –
    "\u2018": "'",   # left single quote '
    "\u2019": "'",   # right single quote '
    "\u201c": '"',   # left double quote "
    "\u201d": '"',   # right double quote "
    "\u2026": "...", # ellipsis …
    "\u2022": "-",   # bullet •
    "\u00a0": " ",   # non-breaking space
}


def _safe(text) -> str:
    """Make text safe for latin-1 encoding used by core PDF fonts."""
    text = str(text)
    for src, dst in _UNICODE_REPLACEMENTS.items():
        text = text.replace(src, dst)
    return text.encode("latin-1", errors="replace").decode("latin-1")


def _hex_to_rgb(rating_color_hex: str):
    h = rating_color_hex.lstrip("#")
    return tuple(int(h[i:i + 2], 16) for i in (0, 2, 4))


class ReportPDF(FPDF):
    def header(self):
        self.set_fill_color(*DARK)
        self.rect(0, 0, self.w, 26, style="F")
        self.set_xy(12, 8)
        self.set_font("Helvetica", "B", 15)
        self.set_text_color(255, 255, 255)
        self.cell(0, 8, "Employee Job Satisfaction Prediction Report", ln=1)
        self.set_xy(12, 16)
        self.set_font("Helvetica", "", 9)
        self.set_text_color(180, 190, 205)
        self.cell(0, 6, "Generated by the ML Prediction Dashboard", ln=1)
        self.ln(12)

    def footer(self):
        self.set_y(-15)
        self.set_font("Helvetica", "", 8)
        self.set_text_color(*MUTED)
        self.cell(0, 10, _safe(f"Page {self.page_no()}"), align="C")


def build_prediction_report(inputs: dict, result, department_display: str) -> bytes:
    """
    inputs: dict of raw employee input values
    result: predict.PredictionResult instance
    """
    pdf = ReportPDF(format="A4", unit="mm")
    pdf.set_auto_page_break(auto=True, margin=20)
    pdf.add_page()

    accent_rgb = _hex_to_rgb(result.color)

    # --- Timestamp ---
    pdf.set_font("Helvetica", "", 10)
    pdf.set_text_color(*MUTED)
    pdf.cell(0, 6, _safe(f"Generated on: {datetime.now().strftime('%B %d, %Y at %I:%M %p')}"), ln=1)
    pdf.ln(4)

    # --- Prediction summary box ---
    box_y = pdf.get_y()
    pdf.set_fill_color(245, 247, 250)
    pdf.set_draw_color(*accent_rgb)
    pdf.set_line_width(0.6)
    pdf.rect(12, box_y, pdf.w - 24, 34, style="DF")

    pdf.set_xy(18, box_y + 6)
    pdf.set_font("Helvetica", "B", 24)
    pdf.set_text_color(*accent_rgb)
    pdf.cell(40, 12, _safe(f"{result.rating}/5"), ln=0)

    pdf.set_xy(60, box_y + 7)
    pdf.set_font("Helvetica", "B", 13)
    pdf.set_text_color(*TEXT)
    pdf.cell(0, 8, _safe(result.label), ln=1)

    pdf.set_xy(60, box_y + 16)
    pdf.set_font("Helvetica", "", 10)
    pdf.set_text_color(*MUTED)
    pdf.cell(0, 6, _safe(f"Risk Level: {result.risk}   |   Confidence: {result.confidence}%"), ln=1)

    pdf.set_y(box_y + 40)

    # --- Confidence bar ---
    pdf.set_font("Helvetica", "B", 10)
    pdf.set_text_color(*TEXT)
    pdf.cell(0, 6, "Confidence Score", ln=1)
    bar_y = pdf.get_y() + 1
    bar_w = pdf.w - 24
    pdf.set_fill_color(230, 233, 240)
    pdf.rect(12, bar_y, bar_w, 5, style="F")
    fill_w = max(4, bar_w * (result.confidence / 100))
    pdf.set_fill_color(*accent_rgb)
    pdf.rect(12, bar_y, fill_w, 5, style="F")
    pdf.ln(12)

    # --- Input values table ---
    pdf.set_font("Helvetica", "B", 12)
    pdf.set_text_color(*TEXT)
    pdf.cell(0, 8, "Employee Input Details", ln=1)
    pdf.set_draw_color(220, 224, 232)

    rows = [
        ("Department", department_display),
        ("Age", f"{inputs['Age']} years"),
        ("Experience", f"{inputs['Experience_Years']} years"),
        ("Salary", f"Rs. {inputs['Salary_PKR']:,.0f}"),
        ("Work-Life Balance", f"{inputs['Work_Life_Balance']} / 5"),
        ("Manager Support", f"{inputs['Manager_Support']} / 5"),
        ("Promotion (Last 2 Years)", inputs["Promotion_Last_2Y"]),
    ]
    pdf.set_font("Helvetica", "", 10)
    for i, (label, value) in enumerate(rows):
        fill = 250 if i % 2 == 0 else 255
        pdf.set_fill_color(fill, fill + 1 if fill < 255 else 255, fill + 3 if fill < 255 else 255)
        pdf.cell(70, 8, _safe(label), border=0, fill=True)
        pdf.set_text_color(*MUTED)
        pdf.cell(0, 8, _safe(value), border=0, fill=True, ln=1)
        pdf.set_text_color(*TEXT)

    pdf.ln(4)

    # --- Recommendation ---
    pdf.set_font("Helvetica", "B", 12)
    pdf.cell(0, 8, "Recommendation", ln=1)
    pdf.set_font("Helvetica", "", 10)
    pdf.set_text_color(*MUTED)
    pdf.set_x(pdf.l_margin)
    pdf.multi_cell(0, 6, _safe(result.recommendation))
    pdf.ln(2)

    # --- Reasoning ---
    if result.reasoning:
        pdf.set_font("Helvetica", "B", 12)
        pdf.set_text_color(*TEXT)
        pdf.cell(0, 8, "Prediction Reasoning", ln=1)
        pdf.set_font("Helvetica", "", 10)
        pdf.set_text_color(*MUTED)
        for point in result.reasoning:
            pdf.set_x(pdf.l_margin)
            pdf.multi_cell(0, 6, _safe(f"- {point}"))
        pdf.ln(2)

    pdf.set_text_color(*TEXT)

    return _get_pdf_bytes(pdf)


def _get_pdf_bytes(pdf: "ReportPDF") -> bytes:
    """
    Return the finished PDF as raw bytes, regardless of whether the
    installed package is the modern `fpdf2` or the legacy `fpdf`.

    Calling output(dest="S") is supported by both:
      - legacy fpdf: returns the PDF content as a `str`.
      - fpdf2 (all versions to date): returns a `bytearray` (dest is
        deprecated but still honored, so this avoids version-specific
        branching that only some installs support).

    IMPORTANT: we deliberately never call output() with no arguments,
    because legacy fpdf treats that as "print PDF to stdout" (dest='I')
    and returns an empty string instead of the document bytes.
    """
    try:
        result = pdf.output(dest="S")
    except TypeError:
        # Some future fpdf2 release may drop the deprecated `dest` kwarg.
        result = pdf.output()

    if isinstance(result, str):
        return result.encode("latin-1", errors="replace")
    if isinstance(result, (bytes, bytearray)):
        return bytes(result)
    raise RuntimeError(f"Unexpected PDF output type: {type(result)}")